'''
this program  prints the sum of two floating point numbers, 
the difference between two integers, 
and the product of a floating point number and an integer
'''
x = 7.0 #floating numbers are declared with a .0 after the intial number
y = 5.0
z = 6

print("x + y = ", x+y)
print(type(x+y)) #the type() function returns the data type of the answer

print("x - y = ", x-y)
print(type(x-y))

print("x * z = ", x*z)
print(type(x*z))